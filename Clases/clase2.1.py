#Operaciones
#Variables "int"
numeroA = 2
numeroB = 10
#Operación de suma
sumar = numeroA + numeroB
#Mostrar el resultado de la operación suma por medio de la función print
print ("El resultado de la suma es", sumar)
print (f"La suma dió {sumar} exitosamente")
#Operación resta
restar = numeroA - numeroB
print (f"La resta dió {restar} exitosamente")
#Operación multiplicación
multiplicar = numeroA * numeroB
print (f"La multiplicación dió {multiplicar} exitosamente")
#Operación división
dividir = numeroA / numeroB
print (f"La dividisión dió {dividir} exitosamente")
#Operación exponencial
exponente = numeroA ** numeroB
print (f"La operación exponencial dió {exponente} exitosamente")
